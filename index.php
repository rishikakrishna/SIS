                <h2 class="section-title"><span>History</span></h2>
                <div class="spacer"></div>
                <p class="section-subtitle">Students of the Indian Subcontinent at The University of Oregon</p>
            </header>
            <div class="row">
                <div class="col-md-12">
                    <p>Founded in 1999, Students of the Indian Subcontinent was found as a group that helped network South Asian students (including those from Bangladesh, India, Nepal, Pakistan and Sri Lanka) and to allow them to reconnect with their cultural
                        roots. We began in a small way by organizing pot-luck dinners and socials like Diwali, and movie night. Our annual festival, Utsav, the showcase of cultural activities of SIS, was first organized in 1998. Utsav is organized in
                        late winter to herald the arrival of Spring. SIS has members with origins in the subcontinent who have all converged in Eugene. This diversity has helped in promoting better understanding about the issues that are faced by people
                        living in the subcontinent. SIS is growing from strength to strength all the time, not just in its membership, but also in its activities. We are represented in most of the departments in the university, thus enabling us to reach
                        out to prospective students from all areas of interest.</p>
